public class Akali {
    private String damage;//攻击力
    private String ability_power;//法强
    private String Armor;//护甲
    private String spell_resistance;//魔抗
    private String speed;

    public void setDamage(String damage) {
        this.damage = damage;
    }

    public String getDamage() {
        return damage;
    }

    public void setAbility_power(String ability_power) {
        this.ability_power = ability_power;
    }

    public String getAbility_power() {
        return ability_power;
    }

    public void setArmor(String armor) {
        Armor = armor;
    }

    public String getArmor() {
        return Armor;
    }

    public void setSpell_resistance(String spell_resistance) {
        this.spell_resistance = spell_resistance;
    }

    public String getSpell_resistance() {
        return spell_resistance;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }

    public String getSpeed() {
        return speed;
    }
}
